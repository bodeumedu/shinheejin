const THEME_MAP = Object.fromEntries(SUM15_THEME_PRESETS.map((theme) => [theme.id, theme]))

export function getSum15Theme(themeId = 'classic') {
  return THEME_MAP[themeId] || THEME_MAP.classic
}

export function getSum15ThemeCssVars(themeId = 'classic') {
  const { colors } = getSum15Theme(themeId)

  return {
    '--sum15-page-bg': colors.pageBg,
    '--sum15-ink': colors.ink,
    '--sum15-muted': colors.muted,
    '--sum15-source-accent': colors.sourceAccent,
    '--sum15-passage-accent': colors.passageAccent,
    '--sum15-interview-accent': colors.interviewAccent,
    '--sum15-panel-bg-start': colors.panelBgStart,
    '--sum15-panel-bg-end': colors.panelBgEnd,
    '--sum15-choice-bg-start': colors.choiceBgStart,
    '--sum15-choice-bg-end': colors.choiceBgEnd,
    '--sum15-panel-border': colors.panelBorder,
    '--sum15-answer-bg': colors.answerBg,
    '--sum15-answer-title': colors.answerTitle,
    '--sum15-answer-accent': colors.answerAccent,
    '--sum15-page-shadow': colors.shadow,
    '--sum15-panel-shadow': colors.panelShadow,
    '--sum15-answer-shadow': colors.answerShadow,
  }
}
